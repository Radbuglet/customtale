use anyhow::Context;
use bytes::{Bytes, BytesMut};

use crate::packets::AnyPacket;

fn check_round_trip(name: &'static str, id: u32, data: &'static [u8]) {
    if let Err(err) = check_round_trip_fallible(name, id, data) {
        panic!("{err:?}");
    }
}

fn check_round_trip_fallible(
    name: &'static str,
    id: u32,
    data: &'static [u8],
) -> anyhow::Result<()> {
    if AnyPacket::descriptor_for(id).is_none() {
        eprintln!("(skipping {name})");

        return Ok(());
    }

    let decoded = AnyPacket::decode(id, Bytes::from_static(data))
        .with_context(|| format!("failed to decode {name}"))?;

    let mut target = BytesMut::new();
    decoded
        .encode(&mut target)
        .with_context(|| format!("failed to encode {name}"))?;

    assert_eq!(target, data, "failed to round-trip {name}");

    Ok(())
}

